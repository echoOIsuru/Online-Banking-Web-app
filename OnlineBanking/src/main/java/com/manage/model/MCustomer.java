package com.manage.model;

public class MCustomer {
	private String mID;
	private String mName;
	private String mCardNumber;
	private String mCardType;

	/*
	 * return customer id
	 */
	public String getmID() {
		return mID;
	}

	public void setmID(String mID) {
		this.mID = mID;
	}

	/*
	 * return customer card name
	 */
	public String getmName() {
		return mName;
	}

	public void setmName(String mName) {
		this.mName = mName;
	}

	/*
	 * return customer card number
	 */
	public String getmCardNumber() {
		return mCardNumber;
	}

	public void setmCardNumber(String mCardNumber) {
		this.mCardNumber = mCardNumber;
	}

	/*
	 * return customer card type
	 */
	public String getmCardType() {
		return mCardType;
	}

	public void setmCardType(String mCardType) {
		this.mCardType = mCardType;
	}


}
